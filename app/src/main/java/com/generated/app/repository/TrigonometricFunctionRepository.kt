package com.generated.app.repository

class TrigonometricFunctionRepository {
    fun calculateSine(angle: Double): Double {
        return kotlin.math.sin(angle)
    }

    fun calculateCosine(angle: Double): Double {
        return kotlin.math.cos(angle)
    }

    fun calculateTangent(angle: Double): Double {
        return kotlin.math.tan(angle)
    }
}